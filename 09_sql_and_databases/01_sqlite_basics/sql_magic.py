 
# JUPYTER NOTEBOOK MAGIC COMMANDS
 
# TYPES OF MAGIC COMMANDS
 
# %    -> Line Magics   (apply to a single line)
# %%   -> Cell Magics   (apply to the entire cell)


# LINE MAGIC COMMANDS
 

"""
%pwd                -> Prints the current working directory

%ls                 -> Lists all files in the current directory

%history            -> Shows the command history

%reset              -> Resets the namespace and deletes all user-defined variables

%who                -> Lists all variables in the namespace

%whos               -> Shows detailed information about all variables

%matplotlib inline  -> Displays matplotlib plots inside the notebook

%timeit             -> Measures the execution time of a single statement

%lsmagic            -> Shows all available magic commands
"""


 
